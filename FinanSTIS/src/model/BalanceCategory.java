public class BalanceCategory {
    private int id;
    private String name;

    // Konstruktor default
    public BalanceCategory() {}

    // Konstruktor dengan parameter
    public BalanceCategory(int id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getter dan Setter
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
